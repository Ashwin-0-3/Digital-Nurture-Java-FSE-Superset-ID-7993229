public class ForeignCar {
    String x;
    String y;
    String z;
    public ForeignCar(String x,String y,String z)
    {
        this.x=x;
        this.y=y;
        this.z=z;
    }
    public String b() {
        return x;
    }
    public String m() {
        return y;
    }
    public String c() {
        return z;
    }
}
